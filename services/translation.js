// Language detection
const languageDetector = {
  type: 'languageDetector',
  async: true,
  detect: (callback) => {
    AsyncStorage.getItem('appLanguage').then((language) => {
      callback(language || 'en');
    });
  },
  init: () => {},
  cacheUserLanguage: (language) => {
    AsyncStorage.setItem('appLanguage', language);
  }
};

i18n
  .use(languageDetector)
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: 'en',
    debug: false,
    interpolation: {
      escapeValue: false,
    },
    react: {
      useSuspense: false,
    },
  });

export default i18n;